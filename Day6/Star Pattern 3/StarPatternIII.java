class StarPatternIII{
	public static void main(String args[]){
	for(int i=1;4>=i;i++){
		for(int j=3;j>=i;j--){
			System.out.print(" ");
			}
		for(int k=1; i>=k;k++){	
			System.out.print("*");
			}
		for(int l =2; i>=l;l++){
			System.out.print("*");
			}
		System.out.println();
        }
	}
}	